namespace FluentCards;

/// <summary>
/// Specifies the style of a container.
/// </summary>
public enum ContainerStyle
{
    /// <summary>
    /// Default container style.
    /// </summary>
    Default,

    /// <summary>
    /// Emphasis container style.
    /// </summary>
    Emphasis,

    /// <summary>
    /// Good (success) container style.
    /// </summary>
    Good,

    /// <summary>
    /// Attention (error) container style.
    /// </summary>
    Attention,

    /// <summary>
    /// Warning container style.
    /// </summary>
    Warning,

    /// <summary>
    /// Accent container style.
    /// </summary>
    Accent
}
