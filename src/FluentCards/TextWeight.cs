namespace FluentCards;

/// <summary>
/// Specifies the weight (boldness) of text in a TextBlock.
/// </summary>
public enum TextWeight
{
    /// <summary>
    /// Lighter than normal weight.
    /// </summary>
    Lighter,

    /// <summary>
    /// Normal (default) weight.
    /// </summary>
    Default,

    /// <summary>
    /// Bolder than normal weight.
    /// </summary>
    Bolder
}
