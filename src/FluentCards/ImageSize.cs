namespace FluentCards;

/// <summary>
/// Specifies the size of images in an ImageSet.
/// </summary>
public enum ImageSize
{
    /// <summary>
    /// Automatically size the image.
    /// </summary>
    Auto,

    /// <summary>
    /// Stretch the image to fit.
    /// </summary>
    Stretch,

    /// <summary>
    /// Small image size.
    /// </summary>
    Small,

    /// <summary>
    /// Medium image size.
    /// </summary>
    Medium,

    /// <summary>
    /// Large image size.
    /// </summary>
    Large
}
