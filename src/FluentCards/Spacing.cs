namespace FluentCards;

/// <summary>
/// Specifies the spacing between elements.
/// </summary>
public enum Spacing
{
    /// <summary>
    /// Default spacing.
    /// </summary>
    Default,

    /// <summary>
    /// No spacing.
    /// </summary>
    None,

    /// <summary>
    /// Small spacing.
    /// </summary>
    Small,

    /// <summary>
    /// Medium spacing.
    /// </summary>
    Medium,

    /// <summary>
    /// Large spacing.
    /// </summary>
    Large,

    /// <summary>
    /// Extra large spacing.
    /// </summary>
    ExtraLarge,

    /// <summary>
    /// Padding spacing.
    /// </summary>
    Padding
}
