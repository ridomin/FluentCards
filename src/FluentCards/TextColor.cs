namespace FluentCards;

/// <summary>
/// Specifies the color of text in a TextBlock.
/// </summary>
public enum TextColor
{
    /// <summary>
    /// Default color.
    /// </summary>
    Default,

    /// <summary>
    /// Dark color.
    /// </summary>
    Dark,

    /// <summary>
    /// Light color.
    /// </summary>
    Light,

    /// <summary>
    /// Accent color.
    /// </summary>
    Accent,

    /// <summary>
    /// Good (success) color, typically green.
    /// </summary>
    Good,

    /// <summary>
    /// Attention (error) color, typically red.
    /// </summary>
    Attention,

    /// <summary>
    /// Warning color, typically yellow/orange.
    /// </summary>
    Warning
}
