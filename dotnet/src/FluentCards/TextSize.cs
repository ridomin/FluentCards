/// </summary>
public enum TextSize
{
    /// <summary>
    /// Small text size.
    /// </summary>
    Small,

    /// <summary>
    /// Default text size.
    /// </summary>
    Default,

    /// <summary>
    /// Medium text size.
    /// </summary>
    Medium,

    /// <summary>
    /// Large text size.
    /// </summary>
    Large,

    /// <summary>
    /// Extra large text size.
    /// </summary>
    ExtraLarge
}
