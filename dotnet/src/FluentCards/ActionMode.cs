namespace FluentCards;

/// <summary>
/// Specifies the mode of an action.
/// </summary>
/// <remarks>Added in Adaptive Cards 1.5.</remarks>
public enum ActionMode
{
    /// <summary>
    /// The action is displayed as a primary action.
    /// </summary>
    Primary,

    /// <summary>
    /// The action is displayed as a secondary action (e.g., in an overflow menu).
    /// </summary>
    Secondary
}
